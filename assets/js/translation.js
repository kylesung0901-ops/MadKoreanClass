// Get current language from localStorage or default to 'en'
let currentLanguage = localStorage.getItem('language') || 'en';

function toggleLanguage() {
    // Toggle between EN and KR
    currentLanguage = currentLanguage === 'en' ? 'kr' : 'en';

    // Save to localStorage
    localStorage.setItem('language', currentLanguage);

    // Update button text
    const langBtn = document.getElementById('currentLang');
    if (langBtn) langBtn.textContent = currentLanguage.toUpperCase();

    // Apply translations
    applyTranslations();
}

function applyTranslations() {
    const lang = translations[currentLanguage];

    // Update all translatable elements
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (lang[key]) {
            // Handle placeholders for inputs
            if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = lang[key];
            } else {
                element.innerHTML = lang[key];
            }
        }
    });

    // Special handling for mixed text elements if any (usually manual data-translate is better)
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function () {
    const langBtn = document.getElementById('currentLang');
    if (langBtn) langBtn.textContent = currentLanguage.toUpperCase();
    applyTranslations();
});
